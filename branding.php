		<h2>The design process</h2>
		<p>The other designer and I teamed up to create a new design language that better represented Fooda and its brand values.
			We began the process by interviewing customers and company employees in order to get a better sense of how 
			people currently viewed Fooda and how we wanted to portray ourselves.  We decided that the best way to go about creating the new look was
			to go through a two week design sprint.  We locked ourselves in a small conference room for 10 days, covered the 
			walls with brand inspiration, sketches, and any idea that popped into our heads.  Our goal was to get every idea
			out of our heads and onto paper  - no idea was a bad idea.  This helped us to think more freely
			and not self-critique ideas even if they were pretty outlandish.  We sketched, critiqued, debated,
			created mock-ups in Illustrator, and debated some more.  It was an intense two weeks but we left with something we were 
			both proud of, and more importantly, visually represented our brand.  I later documented 
			our design language in both digital and print versions of a Fooda style guide.  It includes visual assets, fonts, logos,
			 photos and guidelines around where and how to use them.  The style guide was distributed to the entire company and was the 
			first step in ensuring Fooda's visual identity be cohesive accross all internal and consumer-facing visual material.
		</p>
