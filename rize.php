				<p>Freelance graphic designer</p>
				<h2>The design process</h2>
				<p>Athletics were a big part of my life while I was growing up, so I had a natural connection with this project. 
					To start, I wanted to get a sense of the culture of the company. To do this, 
					I talked with the founder of Rize about the ins and outs of the company. The major goal of Rize is to teach kids 
					how to train effectively and get better in their respective sports. To communicate this visually, I designed an 
					“R” in which the top of the letter slopes upward in the shape of an arrow to represent improvement and growth. 
					I also gave the “R” a wide base and triangular cutout to evoke strength and flair. The lines and curves are sleek 
					and make the logo even more distinctive, which will help people to recognize and identify the brand. 
				</p>
				<h2>Tweaking the logo</h2>
				<p>Once I had the basic direction of the logo set and confirmed with the founder of Rize, we began a back and forth, 
					collaborative iteration process to iron out the specifics (exact hue of blue, font, size, etc.). We went through 
					about fifty variations of the “R" before we reached the end goal. We also sampled about a hundred different fonts 
					for the “Rize Sports,” which sits beneath the logo. All in all, we went through over 150 iterations before landing 
					on the final version.</p>
