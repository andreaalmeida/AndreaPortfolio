		<h2>My role</h2>
		<p>Design lead</p>

		<h2>About this project</h2>
		<p>Fooda partners with local restaurants to offer daily rotating options for lunch.  Each day a different restaurant comes and sells
			their food at a designated Fooda Popup location.  The food court at The Shops at North Bridge became the first permanent public
			popup site, and I was asked to design the space.  It was an exciting new challenge to step back from designing in 2D
			to 3D.
		</p>

		<h2>The design process</h2>
		<p>To kick off the project, I met with the the Fooda sales team, restaurant management team, managers in charge of the space at the Shops
			at North Bridge as well as the contractor.  I wanted to get a good sense of what was possible in this space.  I visited the site many 
			times in order to get 
			a better sense of the layout, the lighting, and how customers would approach the space and walk through it.  I wanted to make the 
			physical experience of seeing a menu and ordering food quick, easy, and enjoyable.  I began the design process by created digital renditions 
			of the space, first in black and white "wireframes"
			 and then as full-color mock-ups.  I made many trips to home imporovement stores, hauled 70 square feet of wood planks into my apartment and white washed
			 it so that it could be used on one of the walls.  Once the physical space was designed, I turned my efforts to marketing - creating a message
			 that would help draw in people in the mall to come visit the Fooda Popup.  I designed handouts, signs, emails, and a motion graphic
			 video advertising Fooda, which now plays near the entrance of the mall.  The site officially launched November 16, 2015
		</p>

