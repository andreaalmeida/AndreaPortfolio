				<h3>My role</h3>
				<p>Design lead</p>
				<h3>How it works</h3>
				<p>Fooda partners with local restaurants to offer daily rotating options for lunch.  Each day, a different restaurant 
					visits a designated Fooda space (called a popup site) and sells their food.
				</p>
				<h3>The design process</h3>
				<p>I led the design of a Fooda popup site at a downtown Chicago mall.   I began by familiarizing myself with the 
					popup space and observing the flow of traffic throughout the food court. I put myself in the customer’s shoes, 
					envisioning what it would be like to read a menu, order food, and pay for my lunch.  I wanted to design a process 
					that would be smooth and enjoyable for future customers.
				</p>
				<h3>Challenges</h3>
				<p>A small budget drove me to become more inventive and resourceful.  The space was oddly shaped and one of the
				 walls had uneven edges and awkwardly jutted out.  I wanted to clean it up and create a wall that looked
				  purposeful and served as a backdrop for restaurant menus. I decided to cover the wall in wood planks.  
				  However, a white-washed wood planked wall was too expensive to buy, so  I had to make it myself. I drove
				   to Home Depot, loaded 70 square feet of lumber into my ‘93 Accord, and hauled it up to my 12th floor 
				   apartment.  I covered my entire floor in wood planks and painted them white.  The whole process took 
				   16 hours and multiple coats of paint, but I got a new facade out of it for a third of the price.
				</p>
				<h3>The marketing campaign</h3>
				<p>Once the design of the Fooda space was complete, I created a marketing campaign around it. I organized a 
					photoshoot, took and edited photos, created emails, wrote copy, designed and printed banners, and put 
					together a video ad.  I stayed up late for several nights watching After Effects tutorials so that I 
					could create a motion graphic video to promote Fooda.  The 15 second video now plays on a 10ft screen 
					at the entrance of The Shops at North Bridge.
				</p>
				<h3>The takeaway</h3>
				<p>During this project, I shed my title of digital product designer and became an architect, an interior 
					designer, a manager, and a painter (the Tom Sawyer variety).  I enjoyed the refreshing autonomy and 
					the variety of problems that I got to solve.  Designing a space, coordinating the installation of 
					materials, and launching a marketing campaign in under 3 weeks was a challenging, but rewarding process.
				</p>
