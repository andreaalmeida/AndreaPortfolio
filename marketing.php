				<h3>My role</h3>
				<p>Facilitator and designer</p>
				<h3>The problem</h3>
				<p>I was inspired by Google Ventures’ 5-day sprint process and implemented it at Fooda to help solve 
					marketing problems. Fooda was looking for a way to increase its sales through marketing.
				</p>
				<h3>The process</h3>
				<p>I organized the project and invited people from different areas of Fooda to participate in the sprint.   
					I brought in the CEO, COO, VP of sales, marketing manager, product manager, QA engineers, and sales 
					team members.  I asked people to give short presentations about the problem as it related to their 
					respective departments.  The CEO led us through the business opportunity and the VP of sales explained 
					the sales cycle. Throughout the week, we worked as a team to come up with a solution.  It was a 
					unique and valuable experience to have everyone working together on the same level, regardless 
					of their typical status within the company. Titles melted away, which allowed for more genuine 
					collaboration. 
				</p>
				<h3>The week</h3>
				<p>Day 1: Understand the problem</br>
					Day 2: Ideate on possible solutions</br>
					Day 3: Decide on the best solution</br>
					Day 4: Prototype the solution</br>
					Day 5: Test the prototype on users
				</p>
				<h3>The results</h3>
				<p>After just 5 days we had a testable prototype—a printed book that explained what Fooda does and its 
					value for potential clients.  The week following the sprint, I edited the the book, organized a 
					photoshoot, shot and edited photos, and put all the pieces together to create the final version 
					of the book.  I was able to get the finished product out the door in 10 days.</p>
