		<h2>The design process</h2>
		<p>The Fooda marketing team needed marketing collateral and wanted to find out what kinds of collateral and what types 
			of messages would work the best for driving account sales for different types of services.  The sprint allowed us all 
			- stakeholders, marketing team, designers, content writers, and sales people - to be in the same room at the same time, 
			dive into the problem, understand where we should be focusing our efforts, ideate (a lot) on the problem, create prototypes 
			of marketing material, and then test these prototypes on actual users.  All this was done in a 5-day span, and at the end of 
			it, we came out with answers about which marketing efforts work where, and which don’t. We ended this sprint having designed
			a 
			Fooda education book that the sales team can bring to meetings with potential new clients.</p>



