		<h2>An introduction to design sprints</h2>
		<p>A design sprint is a 5-day process for answering critical business questions through design.  
			Each day has a distinct, specific focus, and consists of a number of exercises, which were led by the facilitator (me).  
			On day one we understood the problem, on day two we ideated on solutions, on day three we decided which solutions were 
			the best, on day four we prototyped those solutions, and on day five we tested our prototypes on actual users.</p>

