		<h2>My role</h2>
		<p>Design Lead</p>

		<h2>The importance of it</h2>
		<p>Fooda needed a new website to introduce itself to customers, restaurant partners and investors—a place where it 
			could explain who Fooda is, what is does, and how its product could benefit each person. The existing site 
			didn’t do a good job of describing what the heck Fooda actually did, and I had a hard time finding what I 
			was looking for, even after multiple visits.  I wanted the new site to enable people to quickly find the 
			information relevant to them.  This would would end up benefiting both the user (happy user) and Fooda (happy 
			educated user who isn’t frustrated = potential new customer).
		</p>


		<h2>The design process</h2>
		<h3>The business' needs</h3>
		<p>I talked to as many different people as possible to better understand the business’ needs and the types 
			of users Fooda wanted to attract. Fooda wanted to teach people about what it does, show its value to different user groups, increase 
			the number of users, learn more about the people who didn't use Fooda, allow people to apply for jobs, and showcase the work culture.</p>

		<h3>Becoming the user</h3>
		<p>The main focus for this site was on first time users--people who didn't know too much about Fooda yet. As vividly as I could, I tried to imagine 
		myself as each of these users, and I asked myself: <em>What do I value? Why would I use Fooda? Why wouldn’t I use Fooda? How did I find out about Fooda?
		How did I get to fooda.com and what are my intentions here? What is my state of mind when I get to the site?</em>
		</p>

		
		<h3>The users' needs</h3>
		<p>By understanding the users, I could then begin to see what they found to be valuable (or not valuable). The users ultimately wanted
		to find out what Fooda is and how it works,
		sign up,
		see the menus,
		learn about Fooda’s program for employers,
		order catering,
		see jobs,
		learn about Fooda’s restaurant partnership program,
		contact someone at Fooda,
		and learn what it’s like to work at Fooda.</p>

		<h3>Organizing the site</h3>
		<p>I needed to figure out how to structure the website around the business’ and users’ needs.  But they have a 
			lot of needs, and I don’t want to overwhelm users with too much information!  I needed to figure out which 
			needs really NEEDED to be met.  If a user had a need, but the business had no use for that need, it would 
			be a waste of time to design a site around it.  The same goes for the other way around.  I lined up both 
			sets of needs.  Most of them lined up with each other.  This helped me to see the big important categories 
			that I could use to structure the site. I used the categories to start forming pages - if it didn’t make 
			sense for a category to be it’s own page, I made note of it.  Maybe it didn’t need it’s own page but was 
			still really important and needed to be easily discoverable, like be placed on the homepage.  As I dove deeper 
			into each page I again put myself in the user’s shoes (different users depending on the page).  On the 
			restaurant page I tried to imagine myself as a restaurant owner - why would I be coming to the site?  What
			 was i looking for?  What information would be valuable to me?  I did this thought the entire site, and this 
			 is how I cam up with what kind of information to include on each page.


			
		</p>


		<h2>The look and feel</h2>
		<p>The aesthetics of fooda.com match the Fooda brand, which Cam (the other designer) and I set a few months before.  I chose to use a lot of white space 
			to group information, creating smaller sections that the user could focus on one at a time (and easily skip over if it wasn’t relevant to them) .  
			Fooda’s product is food, so I used lots of food photos throughout the site — I think it’s important to show off your product, and photos are a good 
			way to do it.</p>

		<h2>What I learned</h2>
		<p>I won’t always get my way.  A lot of different people wanted a say in what was on the website, how it looked, how it was organized, and how it was worded.  
			I lost the copy battle, which was upsetting. I wanted something simple and friendly and instead we ended up with <em>corporatey</em>-sounding bullet points on a 
			lot of the pages.  Copy is so 
			important to the user experience and affects how people are going to navigate a site, how they perceive a brand, and how they digest information on a page.</p>

		
