		<h2>My role</h2>
		<p>Design lead</p>

		<h2>About this project</h2>
		<p>The Fooda restaurant portal is a web application that enables Fooda restaurant partners to view and download daily customer 
			food delivery orders.  The manifest containing the restaurant's orders is genrated by Fooda and made viewable to restaurants through the portal. Once a restaurant 
			downloads a manifest, it can then begin to prepare the food, which is later picked up and delivered by Fooda.
		</p>

		<h2>The design process</h2>
		<p>I started this project with lots of extensive user interviews. I went on site visits to restaurants, interviewed restaurant managers, cooks,
		and delivery drivers, and observed how a restaurant goes from manifest to delivery-ready meals. My goal was to spot areas where
		this process was defincient and in turn create a solution to better it.  What I learned was that manifests, which were being delivered
		by email at the time were not being delivered on time, delaying the order preparation start time.  This often ended in order delivery
		delays to Fooda customers.  Enter the restaurant portal.  By creating a web app where manifests could always be delivered on time, 
		the belief
		was that the number of late orders would deminish.  I began the process of designing the portal with pen and paper.  I sketched out ideas,
		created user flows, wireframes, and hi-fidelity mock-ups.  Once those were signed off by Fooda execs, I shifted to the front-end.  During this project
		I learned SASS, and incoporated it into my workflow.  It's pretty awesome!  I worked closely with the developers to then impliment my html and compiled 
		CSS into the back-end.</p>

	
