				<h3>How orders work</h3>
				<p>Each morning, Fooda’s restaurant partners receive detailed lists of their online lunch orders, called manifests. As soon as 
					a manifest comes in, the restaurant prints it off, takes it to the kitchen and begins to prepare each meal.  This process 
					is time sensitive—once the manifest is received, each restaurant has about 45 minutes to complete the orders before a 
					delivery driver comes to pick them up.
				</p>
				<h3>The problem</h3>
				<p>Fooda emailed manifests out daily, but as the business grew, so did the number of emails. Too many emails led to technical 
					issues with the mail client, and many began arriving up to 15 minutes late.  Not enough prep time meant orders weren't 
					finished or delivered on time.
				</p>
				<h3>The solution</h3>
				<p>To help create a more reliable way to receive manifests, I designed a restaurant portal.  Restaurants can log into the web 
					app at a designated time each morning and download their manifests. I designed and coded the front-end portion of the 
					site (and taught myself SASS along the way).
				</p>
