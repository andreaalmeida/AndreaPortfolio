				<h3>What Fooda does</h3>
				<p>Fooda partners with local restaurants to curate new menus and deliver lunch to people at work.  The company 
					offers two different services. The first is a delivery service—individual users can view menus online, 
					order lunch, and have it delivered to them in the office.  The second service is called popup—a restaurant 
					comes into the office and sells lunch to employees on site (kind of like having a food truck inside the 
					building).
				</p>
				<h3>The design process</h3>
				<p>A native mobile app would make it easier for customers to see daily menus and/or place an order on the go.  
					I began this project by putting myself in the users’ shoes and thinking through every use case.  I used 
					the use cases to structure the app and put together the user flows.  I designed two different user 
					onboarding experiences—one for new users and another one for returning users.  I created screens to 
					educate new users on how to sign up and I explained why Fooda was asking for personal information 
					(e.g. location). The app is made up of over 120 different screens to account for every use case and user 
					flow.  I  also created a style guide,  which helped Fooda keep the UI consistent on future updates to 
					the app.
				</p>
