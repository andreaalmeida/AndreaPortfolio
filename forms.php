		<h2>The design process</h2>
		<p>
		Forms are a way for users to start a conversation. I wanted to simplify this task for users by keeping forms short
		and to the point.  I created large input fields making them inviting and easy for users to click into.
		Adequate breathing room was left in between form fields so that it is very clear which label corresponds to which
		field.  The end goal was to shrink the reasons users have for abondoning a form - because, yes, they can be long and boring.  
		I chose to document these forms, their styles, and instructions for use as a live style guide so that developers can simply copy 
		and paste the html, css, and/or sass when they needed to include
		a form within an application.
		</p>
