		<h2>Why this topic?</h2>
		<p>I have been a runner since I was twelve.  Running has been my place to meet new friends, 
			connect with myself, push myself, test myself, compete with myself and others.

			and it’s been my not only my competitive outlet, but it’s formed me into the person I am today.  
			I’ve learned to not give up when things get tough—instead do the opposite and push a little harder. 
			 Pushing through something difficult and coming out on top or not — but just allowing yourself to give 
			 that much effort is such an amazing thing - its a huge confidence booster.  The more I ran the more I 
			 fell in love with it.  I competed thorough college and then went on to coach at the BGSU the following year. 
			  It’s been a huge part of my life.  Its an individual and team sport.  You run your race but you also have 
			  teammate who help you, you help them and collectively you are all responsible for the placing of the team 
			  against others.  My best friends to this day are all runners - going for runs together, encouraging each 
			  other, sweating together and just letting go of shit together you form a bond that is not easily severed. 
			   After college I haven’t had too much of a desire to compete.  Every once in a while I’ll get an itch to 
			   race — train for a while and then be content with just running for relaxation.  Running not i still push 
			   myself time to time but it’s also my alone time to just feel to be free.  sometimes I run with a phone and 
			   listen to music other times I just want to run through a park and listen to the birds, the wind, the trees, 
			   the stream.  To reconnect with the world.  Other times I have my watch on, doing reps on the track.  Sometimes 
			   I use an app on my phone that spits out my pace every so often - but now i have a watch for that - which i 
			   choose to use sometimes and other times no.  I never regret going for a run and i wanted to learn more about 
			   running in this connected world.  Today more than ever we are so connected.  Texts, emails, Facebook.  I 
			   find myself constantly checking these things - and it’s almost never a conscious decision.  I even use technology
			    when I run— i carry my phone with me and sometimes people call or text during a workout, and i can’t help 
			    but reply sometimes.  Is that healthy?  What affect does that have on us?  I feel like technology is hurting
			     me more that it is doing good.  With all these advances in technology is there a way to use it for good, to
			      help us disconnect even deeper.  Running is one of the ways I have used to disconnect and have so much 
			      passion about which is why I wanted to focus on it.
		</p>
	
