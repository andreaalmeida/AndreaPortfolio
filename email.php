				<h3>The problem</h3>
				<p>Fooda’s sales, marketing, and operations departments sent dozens of different emails every day, resulting 
					in thousands of customers receiving emails that varied in look and tone. Customers were opening emails 
					that were long and lacked logical information hierarchy, and sooner or later they would begin to 
					associate their email experiences with the Fooda brand. Prior to designing email templates, employees 
					across departments were also wasting valuable time trying to figure out their own layouts for individual 
					emails.
				</p>
				<h3>The solution</h3>
				<p>I initiated and led this project to make Fooda’s emails more valuable and digestible for users. I also 
					wanted to make creating and sending emails less stressful and time consuming for Fooda employees.  
					I designed around a dozen email templates that could fit a variety of different messages and created 
					a digital style guide that could be used throughout the company.
