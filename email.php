	<div class="description__container">
		<h2>My role</h2>
		<p>Design lead</p>

		<h2>The problem</h2>
		<p>Fooda sends dozens of different daily emails to customers, potential customers, restaurant partners, and clients.  These
			emails typically had to be created and sent in the same day, which left very little time for worrying about the 
			aesthetics.  I wanted to change that, so initiated this project - the 
			creation of templates for emails that can be used and re-used to fit any type of marketing or sales email sent by Fooda.
			I wanted to make sure that our brand image was consistent accross all of our user touchpoints and make the user's experience 
			when opening and reading through an email more enjoyable.
		</p>

		<h2>The design process</h2>
		<p>I met with members of the sales, operations, and marketing teams to better understand the types of emails we send 
			on a daily basis, the business need for them as well as how they benefit the end user.  Before desining anything I wanted
			to make sure that we were sending emails that had a clear purpose and benefited the ended user, not just sending them
			to send them.  A lot of the emails being sent were very long and wordy.  Sometimes there is a good reason for a more lengthy
			email, but I wanted to make sure that we used the least amount of words possible when it came to copy so we weren't overwhelming
			our users with really voberse emails.  I broke long paragraphs into smaller sections that are more easily degestable and
			scannable.  Email is such an imporant way to communicate with our users because of the number of users we can reach at one time,
			and I wanted to make it more enjoyable for them to open and read though Fooda emails.  I documented guidelines around email 
			creation that would help make this possible and keep our design consistent with the rest of the consumer facing material.
			</p>
	</div>

